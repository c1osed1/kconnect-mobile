import 'package:dartz/dartz.dart';

import '../error/failures.dart';

/// Базовый класс для Use Case операций, которые могут завершиться ошибкой
///
/// Используется для операций чтения данных, которые могут вернуть Either'<'Failure, T'>'.
/// Обеспечивает централизованную обработку ошибок во всем приложении.
///
/// [T] - тип возвращаемого результата при успехе
/// [Params] - тип параметров для выполнения операции
abstract class UseCase<T, Params> {
  Future<Either<Failure, T>?> call(Params params);
}

/// Класс-заглушка для Use Case операций без параметров
///
/// Используется когда операция не требует входных параметров.
class NoParams {
  const NoParams();
}

/// Базовый класс для командных операций, которые не могут завершиться ошибкой
///
/// Используется для операций записи/обновления данных, которые выполняются
/// в fire-and-forget стиле и не требуют обработки ошибок.
abstract class Command {
  Future<void> call();
}

/// Базовый класс для командных операций с параметрами
///
/// Аналогично [Command], но принимает параметры для выполнения операции.
///
/// [Params] - тип параметров для выполнения команды
abstract class CommandWithParams<Params> {
  Future<void> call(Params params);
}
